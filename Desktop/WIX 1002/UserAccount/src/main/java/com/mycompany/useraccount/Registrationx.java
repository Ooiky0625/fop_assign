    private void btn_registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registerActionPerformed
        String username= txt_username.getText();
        String email= txt_email.getText();
        String password= txt_password.getText();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn= DriverManager.getConnection("jdbc:mysql://localhost:3306/user_account?zeroDateTimeBehavior=CONVERT_TO_NULL", "root", "ooiky");
            String sqlquery= "INSERT INTO userlogin (username, email, userpassword) VALUES (' "+username+ " ' , ' "+email+ " ' , ' "+password+ " ' )";
            PreparedStatement pst= conn.prepareStatement(sqlquery);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Registration Successfully");
            txt_username.setText(" ");
            txt_email.setText(" ");
            txt_password.setText(" ");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_btn_registerActionPerformed


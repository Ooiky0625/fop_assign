    private void btn_checkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_checkActionPerformed
        if(txt_username.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter a username");
        }
        else{
            String username= txt_username.getText();
            try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn= DriverManager.getConnection("jdbc:mysql://localhost:3306/user_account?zeroDateTimeBehavior=CONVERT_TO_NULL", "root", "ooiky");
            String sqlquery="SELECT * FROM userlogin WHERE username=' "+username+" '  ";
            PreparedStatement pst= conn.prepareStatement(sqlquery);
            ResultSet rs= pst.executeQuery();
            if(rs.next()){
                JOptionPane.showMessageDialog(null, "Username is already exist");
            }
            else{
                JOptionPane.showMessageDialog(null, "Username is available");
            }
           }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btn_checkActionPerformed

